    public void ogrencileriGoruntule(){
        DefaultTableModel tblModel=(DefaultTableModel)jTable3.getModel();
               
        tblModel.setRowCount(0);
        try{
          String sql="select* from STUDENTTBL";
          String url="jdbc:oracle:thin:@localhost:1521:XE";
            con=DriverManager.getConnection(url,"sukran","sukran");
            statement=con.createStatement();
            rs=statement.executeQuery(sql);
            while(rs.next()){
                String id=String.valueOf(rs.getInt("STUDENTID"));
                String name=rs.getString("STUDENTUSERNAME");
                String author=rs.getString("STUDENTPASSWORD");
                String description=rs.getString("STUDENTNAME");
                String yearofpub=rs.getString("STUDENTLASTNAME");
                String publisher=rs.getString("STUDENTADDRESS");
                String booktype=rs.getString("STUDENTTEL");
                
                String tbData[]={id,name,author,description,yearofpub,publisher,booktype};
                 tblModel.addRow(tbData);
            }
            con.close();
        }
        catch(Exception ex){
            System.out.println("hata");
        }
      
    }
