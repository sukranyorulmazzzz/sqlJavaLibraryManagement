    public void calisankariGoruntule(){
           DefaultTableModel tblModel=(DefaultTableModel)jTable2.getModel();

        tblModel.setRowCount(0);
        try{
            String sql="select* from LIBRARIANTBL";
            String url="jdbc:oracle:thin:@localhost:1521:XE";
            con=DriverManager.getConnection(url,"sukran","sukran");
            statement=con.createStatement();
            rs=statement.executeQuery(sql);
            while(rs.next()){
                String id=String.valueOf(rs.getInt("LIBRARIANID"));
                String username=rs.getString("LIBRARIANUSERNAME");
                String password=rs.getString("LIBRARIANPASSWORD");
                String name=rs.getString("LIBRARIANNAME");
                String surname=rs.getString("LIBRARIANSURNAME");
                String tel=rs.getString("LIBRARIANTEL");
               

                String tbData[]={id,username,password,name,surname,tel};
                tblModel.addRow(tbData);
            }
            con.close();
        }
        catch(Exception ex){
            System.out.println("hata");
        }

    }
